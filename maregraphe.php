<br><u><center> <h2>-Principe Marégraphe:</h2></center></u>
<p>Un marégraphe est un appareil enregistreur permettant de mesurer le niveau de la mer (ou d'un fleuve) à un endroit donné sur une durée déterminée.
  <br>
Le principe du marégraphe est simple, situé dans un emplacement précisément identifié, le marégraphe enregistre le niveau de la mer au cours du temps. La complexité du marégraphe ne réside donc pas dans son principe mais dans la technologie mise en jeu, pour l'étalonnage des mesures, l'enregistrement, etc.</p>

<u><center><h2>-Instrumentation marégraphique:</h2></center></u>


 <p>Cela fait des siècles que l'homme mesure les hauteurs de l'eau.Plusieurs techniques sont employées pour quantifier cette magnitude.Les plus anciennes restent encore très courantes, voire irremplaçables, comme l'échelle de marée.L'instrumentation a naturellement évolué depuis cette simple échelle de marée qui sert encore aujourd'hui de référence aux technologies modernes comme les marégraphes radar en passant par les marégraphes à flotteur, les marégraphes à pression ou encore les marégraphes à ultrasons.</p>


 <u><h4><center>+Le nilomètre : l'ancêtre de l'échelle de marée.</center></h4></u>
 <p>Le nilometer, est la façon de mesurer les variations de la hauteur de l'eau du Nil.Installé en Égypte dès le 4ème siècle avant JC, le nilomètre se compose d'une échelle graduée qui a permis de quantifier la hauteur d'inondation du Nil .
 <img src="p.jpg" width="600px"/>

<br><br>Contrairement aux nilomètres dont on ne sait pas bien comment les zéros étaient déterminés par les Égyptiens, les échelles de marée, utilisées en France depuis le début du 18ème siècle étaient généralement référencées par rapport à la base des radiers des formes de radoub. C'est notamment le cas pour Brest par exemple où les mesures systématiques démarrent en 1711.
 </p>


 <u><h4><center>+Marégraphe à flotteur et puits de tranquillisation.</center></h4></u>


 <p>
 	L'implantation d'un puits de tranquillisation est une nécessité pour établir un observatoire marégraphique permanent au voisinage d'une côte. Les nombreux marégraphes permanents sont essentiellement localisés dans les zones portuaires ou le long des estuaires navigables. L'utilisation des marégraphes à flotteur tend à disparaître à cause des enregistrements graphiques, dits analogiques mais surtout à cause des erreurs systèmatiques engendrés par la mécanique (frottements du flotteur dans le puits de tranquillisation, usure des pièces mécaniques, ...etc). Ces marégraphes mécaniques sont remplacés au profit de systèmes automatiques d'acquisition de données numériques.<br>
 	 <center><img src="q.jpg" width="600px"/></center>
 </p>


 <u><h4><center>+Marégraphe numérique : marégraphe radar, marégraphe à pression, marégraphe ultrasonique.</center></h4></u>


 <p>Il est peu de disciplines dans le domaine de la physique où s'offre un si grand choix de systèmes de mesure dont plusieurs ont effectivement fait l'objet de développement et d'application en matière de marégraphie.

La configuration du site d'implantation est un facteur à prendre en compte. Les types de capteurs et d'enregistreurs ne sont pas les mêmes d'une part pour les sites côtiers, où l'implantation d'un observatoire marégraphique est possible (ports, estuaires, zone de faibles fonds, et d'autre part pour ceux du large.   
<br><br><img src="c.jpg" width="600px"><img src="d.jpg" width="600px">  
 </p>

 <u><h4><center>+Marégraphes de pression de fond.</center></h4></u>
 <p>La seconde catégorie de marégraphes est constituée de marégraphes à capteur de pression8. Placés au fond de l'eau, ils mesurent la pression environnante. Cette pression est propre à la hauteur de la colonne d'eau et de la pression atmosphérique en surface. Ces marégraphes fonctionnent en parallèle avec une équation qui calculera la hauteur du niveau de la mer par le fond ; d'où pour obtenir les variations du niveau de la mer par l'équation, la connaissance de la pression atmosphérique et de la densité de l'eau est indispensable. L'équation est

