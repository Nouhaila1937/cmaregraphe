 <h2><center><u> -Quel marégraphe choisir ?</h2></u>

  
 <p>Il existe de nombreux fabricants spécialisés dans l'hydrographie ou l'instrumentation industrielle qui propose sur le marché des capteurs radar adaptés à la mesure du niveau de la mer. Le choix du capteur se fait selon le site d'installation (marnage, capteur à l'air libre, type de puits de tranquillisation), l'aspect financier et la qualité de mesure exigée.</p></center>

 
